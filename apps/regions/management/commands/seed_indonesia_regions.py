class Command(BaseCommand):
    help = 'Seed Indonesia region data (provinces, cities, districts, postal codes)'

    @transaction.atomic
    def handle(self, *args, **options):
        self._seed_provinces()
        self._seed_cities()
        self._seed_districts()
        self._seed_postal_codes()
        self._clear_cache()
        self.stdout.write(self.style.SUCCESS(f'Seeding complete.'))

    def _seed_provinces(self):
        for code, name in PROVINCES:
            Province.objects.update_or_create(code=code, defaults={'name': name})
        self.stdout.write(f'  OK {len(PROVINCES)} provinsi')

    def _seed_cities(self):
        count = 0
        for code, province_code, name in CITIES:
            try:
                province = Province.objects.get(code=province_code)
                City.objects.update_or_create(code=code, defaults={
                    'province': province, 'name': name,
                })
                count += 1
            except Province.DoesNotExist:
                self.stdout.write(self.style.WARNING(f'  ! Province {province_code} not found for {name}'))
        self.stdout.write(f'  OK {count} kota/kabupaten')

    def _seed_districts(self):
        count = 0
        for city_code, district_names in DISTRICTS.items():
            try:
                city = City.objects.get(code=city_code)
            except City.DoesNotExist:
                self.stdout.write(self.style.WARNING(f'  ! City {city_code} not found'))
                continue
            for i, name in enumerate(district_names):
                district_code = f'{city_code}{i+1:03d}'
                District.objects.update_or_create(code=district_code, defaults={
                    'city': city, 'name': name,
                })
                count += 1
        self.stdout.write(f'  OK {count} kecamatan')

    def _seed_postal_codes(self):
        count = 0
        for city_code, districts in POSTAL_CODES.items():
            for district_name, code in districts.items():
                try:
                    district = District.objects.get(
                        city__code=city_code, name=district_name,
                    )
                    PostalCode.objects.update_or_create(
                        district=district, code=code,
                        defaults={'district': district},
                    )
                    count += 1
                except District.DoesNotExist:
                    self.stdout.write(self.style.WARNING(
                        f'  ! District "{district_name}" in {city_code} not found'
                    ))
        if not count:
            return
        self.stdout.write(f'  OK {count} kode pos')

    def _clear_cache(self):
        from django.core.cache import cache
        keys = ['regions:provinces']
        for p in Province.objects.values_list('id', flat=True):
            keys.append(f'regions:cities:{p}')
        for c in City.objects.values_list('id', flat=True):
            keys.append(f'regions:districts:{c}')
        for d in District.objects.values_list('id', flat=True):
            keys.append(f'regions:postal_codes:{d}')
        cache.delete_many(keys)
